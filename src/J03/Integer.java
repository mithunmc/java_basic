package J03;

public class Integer {
    public static void main(String[] args) {
        int x = 123_456;
        System.out.println("x: "+x);
        int y = 0b110000;
        System.out.println("y: "+y);
        String s = "23";
        int z = java.lang.Integer.parseInt(s);
        System.out.println("z: "+z);
    }

}
